-module(demo8_event).

-export([start_link/0, stop/0]).
-export([notify/1]).

%% ----------------------------------------------------------------------------
%%  O & M functions
%% ----------------------------------------------------------------------------

start_link() ->
    gen_event:start_link ({local, ?MODULE}).

stop() ->
    gen_event:stop(?MODULE).

%% ----------------------------------------------------------------------------
%%  API functions
%% ----------------------------------------------------------------------------

notify(Event) ->
    gen_event:notify(?MODULE, Event).
